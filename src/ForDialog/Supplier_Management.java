    private void clear()
    {
        txtSupplierId.setText(supplierId + "");
        txtSupplierName.setText("");
        txtPhoneNumber.setText("");
        txtEmail.setText("");
        txtAddress.setText("");
    }
    
    private Supplier getSupplierDataFromComponenets()
    {
        Supplier supplier = new Supplier();
        
        supplier.setSupplierId(Integer.parseInt(txtSupplierId.getText().trim()));
        supplier.setSupplierName(txtSupplierName.getText().trim());
        supplier.setPhoneNumber(txtPhoneNumber.getText().trim());
        supplier.setEmail(txtEmail.getText().trim());
        supplier.setAddress(txtAddress.getText().trim());
        
        return supplier;
    }
    
