    private void btnInsert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsert1ActionPerformed
        try 
        {
            Supplier supplier = getSupplierDataFromComponenets();
            Supplier.addSupplierIntoDB(supplier);
            
            subJTable1.addRow(supplier.getSupplierId(), supplier.getSupplierName(), 
                    supplier.getPhoneNumber(), supplier.getEmail(), supplier.getAddress());
            
            supplierId++;
            
            clear();
            
            JOptionPane.showMessageDialog(this, "Insert Successfully!");
        } 
        catch (HeadlessException e) 
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnInsert1ActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int result = JOptionPane.showConfirmDialog(this, "Are you sure that you want to delete this item?");
        if(result == 0)
        {
            int countSelectedRow = subJTable1.getSelectedRowCount();
            
            for(int index = 0; index < countSelectedRow; index++)
            {
                Object[] r = subJTable1.getRowAt(selectedRowIndex);
                String id = r[0] + "";
                Supplier.deleteSupplierFromDB(Integer.parseInt(id));
            }
            
            subJTable1.removeSelectedRow();
            
            JOptionPane.showMessageDialog(this, "Delete Successfully!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try 
        {
            Supplier supplier = getSupplierDataFromComponenets();
            Supplier.updateSupplierInDB(supplier);
            
            subJTable1.setValueAt(supplier.getSupplierId(), selectedRowIndex, 1);
            subJTable1.setValueAt(supplier.getSupplierName(), selectedRowIndex, 2);
            subJTable1.setValueAt(supplier.getPhoneNumber(), selectedRowIndex, 3);
            subJTable1.setValueAt(supplier.getEmail(), selectedRowIndex, 4);
            subJTable1.setValueAt(supplier.getAddress(), selectedRowIndex, 5);
            
            clear();
            
            JOptionPane.showMessageDialog(this, "Update Successfully!");
        } 
        catch (HeadlessException e) 
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

