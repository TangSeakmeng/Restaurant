    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            Staff s = new Staff();
            
            s.setName(txtStaffName.getText().trim());
            s.setGender(cbGender.getItemAt(cbGender.getSelectedIndex()));
            s.setDateOfBirth(ConvertDateToString.convertDateToString(dtDateOfBirth.getDate()));
            s.setAddress(txtAddress.getText().trim());
            s.setEmail(txtEmail.getText().trim());
            s.setTelephone(txtTelephone.getText().trim());
            s.setImage(path);
            s.setNationalId(txtNationalId.getText().trim());
            s.setPosition(cbPosition.getItemAt(cbPosition.getSelectedIndex()));
            
            subJTable1.addRow(txtStaffId.getText().trim(), 
                    s.getName(), 
                    s.getGender(), 
                    s.getDateOfBirth(), 
                    s.getAddress(), 
                    s.getTelephone(), 
                    s.getEmail(), 
                    s.getPosition(), 
                    s.getNationalId(), 
                    s.getImage());
            
            Staff.addStaffIntoDB(s);
            
            JOptionPane.showMessageDialog(this, "Insert Successfully!");
            
            staffId++;
            clear();
        } 
        catch (HeadlessException e) 
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

