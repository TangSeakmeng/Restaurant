    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try 
        {
            Staff s = new Staff();
            
            s.setStaffId(Integer.parseInt(txtStaffId.getText().trim()));
            s.setName(txtStaffName.getText().trim());
            s.setGender(cbGender.getItemAt(cbGender.getSelectedIndex()));
            s.setDateOfBirth(ConvertDateToString.convertDateToString(dtDateOfBirth.getDate()));
            s.setAddress(txtAddress.getText().trim());
            s.setEmail(txtEmail.getText().trim());
            s.setTelephone(txtTelephone.getText().trim());
            s.setImage(path);
            s.setNationalId(txtNationalId.getText().trim());
            s.setPosition(cbPosition.getItemAt(cbPosition.getSelectedIndex()));
            
            subJTable1.setValueAt(s.getStaffId(), selectedRowIndex, 0);
            subJTable1.setValueAt(s.getName(), selectedRowIndex, 1);
            subJTable1.setValueAt(s.getGender(), selectedRowIndex, 2);
            subJTable1.setValueAt(s.getDateOfBirth(), selectedRowIndex, 3);
            subJTable1.setValueAt(s.getAddress(), selectedRowIndex, 4);
            subJTable1.setValueAt(s.getTelephone(), selectedRowIndex, 5);
            subJTable1.setValueAt(s.getEmail(), selectedRowIndex, 6);
            subJTable1.setValueAt(s.getPosition(), selectedRowIndex, 7);
            subJTable1.setValueAt(s.getNationalId(), selectedRowIndex, 8);
            subJTable1.setValueAt(s.getImage(), selectedRowIndex, 9);
            
            Staff.updateStaffInDB(s);
            
            JOptionPane.showMessageDialog(this, "Update Sucessfully!");
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

